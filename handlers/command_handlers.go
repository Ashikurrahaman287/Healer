// HandleCommand handles the incoming commands from users
func HandleCommand(message *tgbotapi.Message, bot *tgbotapi.BotAPI) {
	command := strings.ToLower(message.Text) // Normalize the command to lowercase

	switch command {
	case "/start":
		handleStartCommand(bot, message)
	case "/sad":
		handleSadCommand(bot, message)
	case "/love":
		handleLoveCommand(bot, message)
	case "/motivation":
		handleMotivationCommand(bot, message)
	case "/patience":
		handlePatienceCommand(bot, message)
	case "/depression":
		handleDepressionCommand(bot, message)
	default:
		sendUnknownCommandMessage(bot, message)
	}
}

// handleStartCommand sends the welcome message when the /start command is triggered
