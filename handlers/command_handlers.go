// sendMessage sends the Ayat to the user
func sendMessage(bot *tgbotapi.BotAPI, chatID int64, message string) {
	msg := tgbotapi.NewMessage(chatID, message)
	_, err := bot.Send(msg)
	if err != nil {
		fmt.Printf("Error sending message: %v\n", err)
	}
}

